<template>
  <v-card width="400">
    <v-container>
      <v-layout row justify-space-around>
        <div>
          <v-btn flat icon class="up_down_bt" style="margin-left: 16px" @mousedown="incTime('years')"><v-icon>arrow_drop_up</v-icon></v-btn>
          <v-btn flat icon class="up_down_bt" style="margin-left: 19px" @mousedown="incTime('months')"><v-icon>arrow_drop_up</v-icon></v-btn>
          <v-btn flat icon class="up_down_bt" style="margin-left: 4px"  @mousedown="incTime('days')"><v-icon>arrow_drop_up</v-icon></v-btn>
          <h1>{{ date }}</h1>
          <v-btn flat icon class="up_down_bt" style="margin-left: 16px" @mousedown="decTime('years')"><v-icon>arrow_drop_down</v-icon></v-btn>
          <v-btn flat icon class="up_down_bt" style="margin-left: 19px" @mousedown="decTime('months')"><v-icon>arrow_drop_down</v-icon></v-btn>
          <v-btn flat icon class="up_down_bt" style="margin-left: 4px"  @mousedown="decTime('days')"><v-icon>arrow_drop_down</v-icon></v-btn>
        </div>
        <div>
        <div>
        <v-tooltip top>
          <v-btn slot="activator" flat icon @click="resetTime" style="margin-top: 5px"><v-icon>restore</v-icon></v-btn>
          <span>{{ $t('ui.date_time_picker.back_realtime') }}</span>
        </v-tooltip>
        </div>
        <div>
        <v-tooltip top>
          <v-btn slot="activator" flat icon @click="togglePauseTime" style="margin-top: 0px"><v-icon>{{ togglePauseTimeIcon }}</v-icon></v-btn>
          <span>{{ $t('ui.date_time_picker.pause_unpause') }}</span>
        </v-tooltip>
        </div>
        </div>
        <div>
          <v-btn flat icon class="up_down_bt" @mousedown="incTime('hours')"><v-icon>arrow_drop_up</v-icon></v-btn>
          <v-btn flat icon class="up_down_bt" @mousedown="incTime('minutes')"><v-icon>arrow_drop_up</v-icon></v-btn>
          <v-btn flat icon class="up_down_bt" @mousedown="incTime('seconds')"><v-icon>arrow_drop_up</v-icon></v-btn>
          <h1>{{ time }}</h1>
          <v-btn flat icon class="up_down_bt" @mousedown="decTime('hours')"><v-icon>arrow_drop_down</v-icon></v-btn>
          <v-btn flat icon class="up_down_bt" @mousedown="decTime('minutes')"><v-icon>arrow_drop_down</v-icon></v-btn>
          <v-btn flat icon class="up_down_bt" @mousedown="decTime('seconds')"><v-icon>arrow_drop_down</v-icon></v-btn>
        </div>
      </v-layout>
    </v-container>
    <div style="padding: 20px">
      <div style="position: absolute">
        <svg height="30" width="360">
          <defs>
            <linearGradient id="grad1" x1="0%" y1="0%" x2="100%" y2="0%">
              <stop v-for="stop in stops" :offset="stop.percent" :style="stop.style" :key="stop.percent"/>
            </linearGradient>
          </defs>
          <rect width="100%" height="100%" fill="url(#grad1)" />
        </svg>
      </div>
      <v-slider min="0" max="1439" style="padding: 0px; width: 360px;" v-model="timeMinute" :hint="sliderHint" persistent-hint></v-slider>
    </div>
  </v-card>
</template>

<script>

import Moment from 'moment'
var clickTimeout
var nbClickRepeat = 0

export default {
  data: function () {
    return {
      stops: [],
      stopCacheKey: {
        sliderStartTime: undefined,
        location: undefined
      }
    }
  },
  props: ['value', 'location'],
  computed: {
    // The MomentJS time in local time
    localTime: {
      get: function () {
        let m = Moment(this.value)
        m.local()
        return m
      },
      set: function (newValue) {
        this.$emit('input', newValue.format())
      }
    },
    time: {
      get: function () {
        return this.localTime.format('HH:mm:ss')
      }
    },
    date: {
      get: function () {
        return this.localTime.format('YYYY-MM-DD')
      }
    },
    timeMinute: {
      get: function () {
        // 0 means 12:00, 720 means midnight, 1440 (=24*60) means 12:00 the day after
        let t = this.localTime
        return t.hours() < 12 ? (t.hours() + 12) * 60 + t.minutes() : (t.hours() - 12) * 60 + t.minutes()
      },
      set: function (newValue) {
        var t = Moment(this.sliderStartTime)
        t.add(newValue, 'minutes')
        this.$emit('input', t.format())
      }
    },
    sliderStartTime: function () {
      let t = this.localTime.clone()
      if (t.hours() < 12) {
        t.subtract(1, 'days')
      }
      t.hours(12)
      t.minutes(0)
      t.seconds(0)
      t.milliseconds(0)
      return t
    },
    sliderHint: function () {
      let tm = this.timeMinute
      let stop = this.stops[Math.floor(tm * this.stops.length / 1440)]
      if (!stop) return ''
      if (stop.sunAlt > 0) {
        return 'Daylight'
      }
      if (stop.sunAlt < -16) {
        return stop.moonAlt < 5 ? 'Dark night' : 'Moonlight'
      }
      return tm > 720 ? 'Dawn' : 'Twilight'
    },
    isTimePaused: function () {
      return this.$store.state.timeSpeed === 0
    },
    togglePauseTimeIcon: function () {
      return this.isTimePaused ? 'play_arrow' : 'pause'
    }
  },
  methods: {
    resetTime: function () {
      let m = Moment()
      m.local()
      this.$emit('input', m.format())
    },
    togglePauseTime: function () {
      this.$store.commit('setValue', {varName: 'timeSpeed', newValue: this.isTimePaused ? 1 : 0})
    },
    incTime: function (unit) {
      this.startIncTime(1, unit)
    },
    decTime: function (unit) {
      this.startIncTime(-1, unit)
    },
    startIncTime: function (v, unit) {
      let that = this
      clickTimeout = setTimeout(_ => {
        let t = this.localTime.clone()
        t.add(v, unit)
        this.$emit('input', t.format())
        nbClickRepeat++
        that.startIncTime(v, unit)
      }, nbClickRepeat === 0 ? 0 : (nbClickRepeat === 1 ? 500 : (nbClickRepeat < 10 ? 100 : (nbClickRepeat < 100 ? 50 : 20))))
    },
    stopIncTime: function () {
      if (clickTimeout) {
        clearTimeout(clickTimeout)
        clickTimeout = undefined
        nbClickRepeat = 0
      }
    },
    // 0 means 12:00, 720 means midnight, 1440 (=24*60) means 12:00 the day after
    timeMinuteRangeToUTC: function (tm) {
      return this.sliderStartTime.toDate().getMJD() + tm * 1 / (24 * 60)
    },
    refreshStops: function () {
      if (this.stopCacheKey.sliderStartTime === this.sliderStartTime.format() && this.stopCacheKey.location === JSON.stringify(this.location)) {
        return
      }
      let res = []
      let nbStop = 49
      let obs = this.$stel.core.observer.clone()
      let sun = this.$stel.getObj('Sun')
      let moon = this.$stel.getObj('Moon')
      for (let i = 0; i <= nbStop; ++i) {
        obs.utc = this.timeMinuteRangeToUTC(1440 * i / nbStop)
        let sunAlt = this.$stel.anpm(this.$stel.c2s(this.$stel.convertFrame(obs, 'ICRF', 'OBSERVED', sun.getInfo('radec', obs)))[1]) * 180.0 / Math.PI
        let moonAlt = this.$stel.anpm(this.$stel.c2s(this.$stel.convertFrame(obs, 'ICRF', 'OBSERVED', moon.getInfo('radec', obs)))[1]) * 180.0 / Math.PI
        let brightnessForAltitude = function (sunAlt, moonAlt) {
          let moonBrightness = moonAlt < 0 ? 0 : 2 / 35 * Math.min(20, moonAlt) / 20
          if (sunAlt > 0) return Math.min(10, 1 + sunAlt) + moonBrightness
          if (sunAlt < -16) return moonBrightness
          if (sunAlt < -10) return 1 / 35 * (16 + sunAlt) / 6 + moonBrightness
          return (1 - 1 / 35) * (10 + sunAlt) / 10 + 1 / 35 + moonBrightness
        }
        let brightness = Math.log10(1 + brightnessForAltitude(sunAlt, moonAlt) * 10) / 2
        res.push({
          percent: i / nbStop,
          style: 'stop-color:rgb(64,209,255);stop-opacity:' + brightness,
          sunAlt: sunAlt,
          moonAlt: moonAlt
        })
      }
      obs.destroy()
      this.stopCacheKey.sliderStartTime = this.sliderStartTime.format()
      this.stopCacheKey.location = JSON.stringify(this.location)
      this.stops = res
    }
  },
  mounted: function () {
    this.refreshStops()
    let that = this
    window.addEventListener('mouseup', function (event) {
      that.stopIncTime()
    })
  },
  watch: {
    sliderStartTime: function () {
      this.refreshStops()
    },
    location: function () {
      this.refreshStops()
    }
  }
}
</script>

